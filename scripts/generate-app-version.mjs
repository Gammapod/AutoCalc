import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = process.cwd();
const packageJsonPath = resolve(root, "package.json");
const outputPath = resolve(root, "src/generated/appVersion.ts");

const packageJsonRaw = await readFile(packageJsonPath, "utf8");
const packageJson = JSON.parse(packageJsonRaw);
const version = String(packageJson.version ?? "").trim();

if (!/^\d+\.\d+\.\d+(-[0-9A-Za-z.-]+)?$/.test(version)) {
  console.error(`Invalid or missing package.json version: "${version}"`);
  process.exit(1);
}

const content = `// Auto-generated by scripts/generate-app-version.mjs
// Do not edit manually.
export const APP_VERSION = "${version}";
`;

await writeFile(outputPath, content, "utf8");
console.log(`APP_VERSION_GENERATED ${version}`);
